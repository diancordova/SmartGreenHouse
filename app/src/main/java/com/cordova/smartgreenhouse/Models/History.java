package com.cordova.smartgreenhouse.Models;

public class History {
    int xValue,yValue;
    public History() {

    }

    public History(int xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public int getxValue() {
        return xValue;
    }



    public int getyValue() {
        return yValue;
    }


}
